def extract_year_tag(qid):
    """qIdから年度タグを抽出"""
    # R7gakkaA-001 → R7年度
    # R6gakkaB-012 → R6年度
    match = re.match(r'^([RH]\d+)', qid)
    if match:
        year = match.group(1)
        return f"{year}年度"
    return ""

def classify_tag1(stem, qid):
    """問題文とqIdから主要分野(tag1)を分類"""
    scores = defaultdict(int)

    text = f"{stem} {qid}"

    for category, keywords in TAG1_KEYWORDS.items():
        for keyword in keywords:
            if keyword in text:
                scores[category] += 1

    if not scores:
        return "その他"

    # 最高スコアのカテゴリを返す
    return max(scores, key=scores.get)

def classify_tag2(tag1, stem):
    """tag1とstemからサブ分野(tag2)を分類"""
    if tag1 not in TAG2_KEYWORDS:
        return ""

    scores = defaultdict(int)

    for subcategory, keywords in TAG2_KEYWORDS[tag1].items():
        for keyword in keywords:
            if keyword in stem:
                scores[subcategory] += 1

    if not scores:
        return ""

    return max(scores, key=scores.get)

def process_csv(input_file):
    """CSVファイルを読み込み、タグを設定して保存"""

    # バックアップ作成
    timestamp = datetime.now().strftime("%Y%m%d_%H%M%S")
    backup_file = input_file.replace('.csv', f'_backup_{timestamp}.csv')

    import shutil
    shutil.copy2(input_file, backup_file)
    print(f"バックアップ作成: {backup_file}")

    # CSVを読み込み
    with open(input_file, 'r', encoding='utf-8-sig', newline='') as f:
        reader = csv.DictReader(f)
        fieldnames = reader.fieldnames
        rows = list(reader)

    print(f"読み込み件数: {len(rows)}行")

    # 統計用カウンター
    tag1_count = defaultdict(int)
    tag2_count = defaultdict(int)
    tag3_count = defaultdict(int)

    # タグ設定
    processed_count = 0
    for row in rows:
        qid = row.get('qId', '')
        stem = row.get('stem', '')

        # qIdが空またはヘッダー行はスキップ
        if not qid or qid == 'qId' or qid.startswith('・'):
            continue

        # tag1: 主要分野
        tag1 = classify_tag1(stem, qid)
        row['tag1'] = tag1
        tag1_count[tag1] += 1

        # tag2: サブ分野
        tag2 = classify_tag2(tag1, stem)
        row['tag2'] = tag2
        if tag2:
            tag2_count[tag2] += 1

        # tag3: 年度
        tag3 = extract_year_tag(qid)
        row['tag3'] = tag3
        if tag3:
            tag3_count[tag3] += 1

        processed_count += 1

    # CSVに書き戻し
    with open(input_file, 'w', encoding='utf-8-sig', newline='') as f:
        writer = csv.DictWriter(f, fieldnames=fieldnames)
        writer.writeheader()
        writer.writerows(rows)

    print(f"\n処理完了: {processed_count}問にタグを設定しました")

    # 統計レポート
    print("\n" + "="*60)
    print("【分野別統計】")
    print("="*60)

    print("\n■ tag1 (主要分野):")
    for category, count in sorted(tag1_count.items(), key=lambda x: x[1], reverse=True):
        print(f"  {category:20s}: {count:3d}問")

    print("\n■ tag2 (サブ分野):")
    for subcategory, count in sorted(tag2_count.items(), key=lambda x: x[1], reverse=True):
        print(f"  {subcategory:20s}: {count:3d}問")

    print("\n■ tag3 (年度):")
    for year, count in sorted(tag3_count.items()):
        print(f"  {year:20s}: {count:3d}問")

    print("\n" + "="*60)
    print(f"ファイル更新完了: {input_file}")
    print("="*60)

